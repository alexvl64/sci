let currentLang = 'en';
function setLang(lang) {
  currentLang = lang;
  try { localStorage.setItem('sc_factsheet_lang', lang); } catch(e){}
  applyLang(lang);
}

(function initLang() {
  let saved = null;
  try { saved = localStorage.getItem('sc_factsheet_lang'); } catch(e){}
  const browser = (navigator.language || navigator.userLanguage || '').toLowerCase();
  const detected = saved || (browser.startsWith('fr') ? 'fr' : 'en');
  currentLang = detected;
  applyLang(detected);
})();

