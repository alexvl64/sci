const SHEET_ID = "1GyKIEU3OS1QQVZ-Qm7EafRlZerNs3nZNHxuFFtkpQPk";
const SHEET_GID = "0";
const period = [], dynamicTrendsValue = [], btcBase100 = [];

async function fetchJsonData() {
  const url = `https://docs.google.com/spreadsheets/d/${SHEET_ID}/gviz/tq?tqx=out:json&gid=${SHEET_GID}`;
  try {
    const r = await fetch(url);
    if (!r.ok) throw new Error(`HTTP ${r.status}`);
    const text = await r.text();
    const data = JSON.parse(text.substring(47).slice(0, -2));
    const locale = currentLang === 'fr' ? 'fr-FR' : 'en-US';
    data.table.rows.forEach((row) => {
      const date = row.c[0]?.v;
      const match = date?.match(/^Date\((\d+),(\d+),(\d+)\)$/);
      period.push(match ? new Date(+match[1], +match[2], +match[3]).toLocaleDateString(locale, {year:'numeric',month:'long'}) : '');
      dynamicTrendsValue.push(parseFloat(row.c[1]?.v) || 0);
      btcBase100.push(parseFloat(row.c[2]?.v) || 0);
    });
    renderChart();
  } catch(e) {
    console.error(e);
    document.getElementById('data-container').textContent = currentLang==='fr' ? 'Impossible de charger les données.' : 'Unable to load data.';
  }
}

function renderChart() {
  new ApexCharts(document.querySelector('#chart-el'), {
    series: [{name:'Dynamic Trends',data:dynamicTrendsValue},{name:'Bitcoin',data:btcBase100}],
    chart: {height:260,type:'line',zoom:{enabled:false},toolbar:{show:false},animations:{enabled:false},background:'transparent'},
    stroke: {width:[2,1.5],dashArray:[0,6]},
    colors: ['#DBD1BC','#545047'],
    xaxis: {categories:period,tickAmount:7,axisTicks:{show:false},axisBorder:{show:false},labels:{hideOverlappingLabels:true,showDuplicates:false,rotate:0,style:{colors:'#6B7B8D',fontFamily:'Inter',fontSize:'9px'}}},
    yaxis: {logarithmic:true,labels:{show:false},axisTicks:{show:false},axisBorder:{show:false}},
    grid: {yaxis:{lines:{show:false}},padding:{left:30,right:14,top:-10,bottom:-8}},
    legend: {show:false},
    tooltip: {theme:'light',style:{fontFamily:'Inter',fontSize:'11px'}},
  }).render();
}

